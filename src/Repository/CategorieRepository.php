class CategorieRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Categorie::class);
    }

    public function save(Categorie $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Categorie $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }

    }
    public function findCategorieByMatricule($matricule)
    {
                return $this->createQueryBuilder('c')
                            ->where('c.matricule LIKE :matricule')
                            ->setParameter('matricule', '%'.$matricule.'%')
                            ->getQuery()
                            ->getResult();
    }
    public function countDistinctMatricules(): int
    {
        return $this->createQueryBuilder('c')
            ->select('COUNT(DISTINCT c.matricule)')
            ->getQuery()
            ->getSingleScalarResult();
    
    }
    public function findAllSortedByMarque($sort = 'asc')
    {
        $queryBuilder = $this->createQueryBuilder('c')
            ->orderBy('c.marque', $sort);
          

    
        return $queryBuilder->getQuery()->getResult();
